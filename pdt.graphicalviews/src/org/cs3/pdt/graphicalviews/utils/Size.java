package org.cs3.pdt.graphicalviews.utils;

public class Size {
	private int width;
	private int height;

	public Size(int width, int height) {
		this.width = width;
		this.height = height;
	}
	
	public int getWidth() {
		return width;
	}
	
	public int getHeight() {
		return height;
	}
}
